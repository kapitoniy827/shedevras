
public class Park {

    // Внутренний класс для хранения информации об аттракционах
    public class Аттракцион {

        private String название;
        private String времяРаботы;  // формат "10:00-19:00"
        private double стоимость;    // цена в рублях

        // Конструктор внутреннего класса
        public Аттракцион(String название, String времяРаботы, double стоимость) {
            this.название = название;
            this.времяРаботы = времяРаботы;
            this.стоимость = стоимость;
        }

        // Геттеры и сеттеры
        public String getНазвание() {
            return название;
        }

        public void setНазвание(String название) {
            this.название = название;
        }

        public String getВремяРаботы() {
            return времяРаботы;
        }

        public void setВремяРаботы(String времяРаботы) {
            this.времяРаботы = времяРаботы;
        }

        public double getСтоимость() {
            return стоимость;
        }

        public void setСтоимость(double стоимость) {
            this.стоимость = стоимость;
        }

        // Метод для вывода информации об аттракционе
        public void показатьИнформацию() {
            System.out.println("Аттракцион: " + название);
            System.out.println("Время работы: " + времяРаботы);
            System.out.println("Стоимость: " + стоимость + " руб.");
            System.out.println("---------------------------");
        }
    }

    // Поле для хранения списка аттракционов
    private Аттракцион[] аттракционы;
    private int размер;

    // Конструктор парка
    public Park(int размерМассива) {
        аттракционы = new Аттракцион[размерМассива];
        размер = 0;
    }

    // Метод добавления аттракциона
    public void добавитьАттракцион(String название, String времяРаботы, double стоимость) {
        if (размер < аттракционы.length) {
            аттракционы[размер] = new Аттракцион(название, времяРаботы, стоимость);
            размер++;
        } else {
            System.out.println("Превышен максимальный размер массива аттракционов!");
        }
    }

    // Метод вывода всех аттракционов
    public void показатьВсеАттракционы() {
        for (int i = 0; i < размер; i++) {
            аттракционы[i].показатьИнформацию();
        }
    }

    // Пример использования
    public static void main(String[] args) {
        // Создаем парк с возможностью хранения 3 аттракционов
        Park парк = new Park(3);

        // Добавляем аттракционы
        парк.добавитьАттракцион("Американские горки", "10:00-19:00", 500.0);
        парк.добавитьАттракцион("Колесо обозрения", "10:00-20:00", 300.0);
        парк.добавитьАттракцион("Комната страха", "14:00-22:00", 400.0);

        // Выводим информацию обо всех аттракционах
        парк.показатьВсеАттракционы();
    }
}
